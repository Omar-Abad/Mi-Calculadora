class Calculadora {
    // Define la clase Calculadora que contiene métodos para realizar operaciones aritméticas
    sumar(num1, num2) {
        // Método para sumar dos números
        return num1 + num2;
        // Devuelve la suma de num1 y num2
    }
    resta(num1, num2) {
        // Método para restar dos números
        return num1 - num2;
        // Devuelve la resta de num1 y num2
    }
    dividir(num1, num2) {
        // Método para dividir dos números
        return num1 / num2;
        // Devuelve la división de num1 entre num2
    }
    multiplicar(num1, num2) {
        // Método para multiplicar dos números
        return num1 * num2;
        // Devuelve la multiplicación de num1 y num2
    }
}
